#define MONGOCXX_VERSION_MINOR
/// The API patch version number for this mongocxx library.
#define MONGOCXX_VERSION_PATCH
/// Additional API version labels for this mongocxx library.
#define MONGOCXX_VERSION_EXTRA

/// @}

#endif  // defined(MONGOCXX_SATISFY_DOXYGEN_DEFINES)

