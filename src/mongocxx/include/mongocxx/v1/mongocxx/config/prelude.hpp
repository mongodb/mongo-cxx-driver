///
/// @file mongocxx/v1/mongocxx/config/prelude.hpp
///
/// Macro guard prelude header for mongocxx.
///
/// Ensures any config macro defined in config headers do not conflict with existing definitions
/// prior to inclusion. To obtain config macros without macro guard behavior, include the
/// corresponding config header directly.
///
/// Prior definitions of conflicting macros are restored by including the @ref
/// mongocxx/v1/mongocxx/config/postlude.hpp "matching postlude header".
///
/// @warning Include of the prelude header twice in a row without a matching include of the postlude
/// header for the first include is forbidden (e.g. prelude -> prelude -> postlude -> postlude).
/// This is to enforce correct use of macro guard headers across transitive includes.
///
/// @warning Include of the postlude header must be preceeded by a matching include of the prelude
/// header.
///
/// @note Include of the matching postlude header may be omitted if restoring prior definitions of
/// conflicting macros is not necessary, e.g. in source (.cpp) files.
///
/// @par Includes
/// @li bsoncxx/v1/bsoncxx/config/prelude.hpp
/// @li @ref mongocxx-v1-mongocxx-config-export-hpp "mongocxx/v1/mongocxx/config/export.hpp"
/// @li @ref mongocxx-v1-mongocxx-config-version-hpp "mongocxx/v1/mongocxx/config/version.hpp"
///

#include <bsoncxx/v1/bsoncxx/config/prelude.hpp>

#if defined(MONGOCXX_V1_INSIDE_MACRO_GUARD_SCOPE)
#error "prelude header included twice without matching postlude"
#endif

/// Used to validate correct use of macro guard headers.
#define MONGOCXX_V1_INSIDE_MACRO_GUARD_SCOPE

// export.hpp (generated by CMake)
#pragma push_macro("MONGOCXX_API_H")
#undef MONGOCXX_API_H
#pragma push_macro("MONGOCXX_API")
#undef MONGOCXX_API
#pragma push_macro("MONGOCXX_PRIVATE")
#undef MONGOCXX_PRIVATE
#pragma push_macro("MONGOCXX_DEPRECATED")
#undef MONGOCXX_DEPRECATED
#pragma push_macro("MONGOCXX_DEPRECATED_EXPORT")
#undef MONGOCXX_DEPRECATED_EXPORT
#pragma push_macro("MONGOCXX_DEPRECATED_NO_EXPORT")
#undef MONGOCXX_DEPRECATED_NO_EXPORT

// version.hpp (generated by CMake)
#pragma push_macro("MONGOCXX_VERSION_STRING")
#undef MONGOCXX_VERSION_STRING
#pragma push_macro("MONGOCXX_VERSION_EXTRA")
#undef MONGOCXX_VERSION_EXTRA
#pragma push_macro("MONGOCXX_VERSION_MAJOR")
#undef MONGOCXX_VERSION_MAJOR
#pragma push_macro("MONGOCXX_VERSION_MINOR")
#undef MONGOCXX_VERSION_MINOR
#pragma push_macro("MONGOCXX_VERSION_PATCH")
#undef MONGOCXX_VERSION_PATCH

#include <mongocxx/v1/mongocxx/config/export.hpp>
#include <mongocxx/v1/mongocxx/config/version.hpp>

